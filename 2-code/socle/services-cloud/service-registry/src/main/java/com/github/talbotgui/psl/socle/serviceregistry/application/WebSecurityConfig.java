package com.github.talbotgui.psl.socle.serviceregistry.application;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.web.SecurityFilterChain;

/** Classe de configuration du service REGISTRE */
@Configuration
public class WebSecurityConfig {

	@Bean
	public SecurityFilterChain filterChain(HttpSecurity http) throws Exception {
		// DÃ©sactivation du CSRF sinon les micro-service ne peuvent pas s'authentifier pour s'enregistrer
		http.csrf(config -> config.ignoringRequestMatchers("/eureka/**"));
		return http.build();
	}
}
